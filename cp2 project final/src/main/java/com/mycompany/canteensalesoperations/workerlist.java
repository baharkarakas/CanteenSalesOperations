    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        if(login.LOGGED_IN_ROLE != 1) {
            JOptionPane.showMessageDialog(this, "Only admins can delete workers");
            return;
        }
        
         if(login.LOGGED_IN_ID == selectedIndex) {
            JOptionPane.showMessageDialog(this, "You cannot delete your own account.");
            return;
        }
        
        try ( PreparedStatement statement = conn.prepareStatement("DELETE FROM canteener WHERE id = ? LIMIT 1")) {

            statement.setInt(1, selectedIndex);
            
            statement.execute();
            
            JOptionPane.showMessageDialog(this, "Worker deleted successfully");
            
            refreshList();
        } catch (SQLException e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        if(login.LOGGED_IN_ROLE != 1) {
            JOptionPane.showMessageDialog(this, "Only admins can reveal worker passwords");
            return;
        }
        
        try ( PreparedStatement statement = conn.prepareStatement("SELECT canteneer_password FROM canteener WHERE id = ?")) {

            statement.setInt(1, selectedIndex);
            
            ResultSet set = statement.executeQuery();
              
            if(!set.next()) {
                JOptionPane.showMessageDialog(null, "No canteneer found with the given id");
                return;
            }
            
            JOptionPane.showMessageDialog(this, set.getString("canteneer_password"));
        } catch (SQLException e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

