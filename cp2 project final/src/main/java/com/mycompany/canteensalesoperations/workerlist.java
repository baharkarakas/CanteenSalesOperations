    void refreshList() {
        try ( PreparedStatement statement = conn.prepareStatement("SELECT * FROM canteener")) {
            ResultSet set = statement.executeQuery();
            
            model = new DefaultTableModel();
            model.setColumnIdentifiers(new String[] {"ID", "Name", "Username", "Role"});
            while(set.next()) {
                model.addRow(new String[] {set.getString("id"),set.getString("canteneer_name"),set.getString("canteneer_username"),set.getInt("canteneer_role") == 1?"Admin":"Worker"});
            }
            
            
            
            jTable1.setModel(model);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

